package cn.link.tcm.po;

public class FormulaTemplate {

	private String zzStr;
	private String zhStr;
	private String zhifaStr;
	private String sourceStr;
	public String getZzStr() {
		return zzStr;
	}
	public void setZzStr(String zzStr) {
		this.zzStr = zzStr;
	}
	public String getZhStr() {
		return zhStr;
	}
	public void setZhStr(String zhStr) {
		this.zhStr = zhStr;
	}
	public String getZhifaStr() {
		return zhifaStr;
	}
	public void setZhifaStr(String zhifaStr) {
		this.zhifaStr = zhifaStr;
	}
	public String getSourceStr() {
		return sourceStr;
	}
	public void setSourceStr(String sourceStr) {
		this.sourceStr = sourceStr;
	}
	@Override
	public String toString() {
		return "FormulaTemplate [zzStr=" + zzStr + ", zhStr=" + zhStr
				+ ", zhifaStr=" + zhifaStr + ", sourceStr=" + sourceStr + "]";
	}
	
	
	
}
